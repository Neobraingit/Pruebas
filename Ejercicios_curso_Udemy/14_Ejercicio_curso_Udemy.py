'''
Crear una variable inicio con el valor 1
Crear una variable fin con el valor 6
Hacer un bucle while que muestre tantas filas como valores haya entre inicio y fin
En cada iteración del bucle mostrar el texto esta es la fila + número de la fila en la que está
'''

inicio = 1
fin = 6

while inicio < fin:
    inicio = inicio + 1
    print ('Esta es la fila', + inicio)
